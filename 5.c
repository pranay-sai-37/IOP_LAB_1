//5. PROGRAM TO SWAP TOW VARIABLES USING THIRD VARIABLE.
#include<stdio.h>
int main()
{
	int t,x,y;
	printf("Enter first number : ");
	scanf("%d",&x);
	printf("Enter second number : ");
	scanf("%d",&y);
	t = x;
	x = y;
	y = t;
	printf("First number : %d",x);
	printf("\nSecond number : %d",y);
	
	
	
	
	
	
	
	
	
	
}

