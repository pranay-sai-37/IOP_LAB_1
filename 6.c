//6. PROGRAM TO SWAP TWO VARIABLES WITHOUT USING THIRD VARIABLE.
#include<stdio.h>
int main()
{
	int x,y;
	printf("First number : ");
	scanf("%d",&x);
	printf("Second number : ");
	scanf("%d",&y);
	x = x+y;
	y = x-y;
	x = x-y;
	printf("First number : %d",x);
	printf("\nSecond number : %d",y);
	
	
	
	
	
	
	
	
	
	
	
	
	
}
